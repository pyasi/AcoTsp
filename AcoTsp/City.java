import java.util.*;

public class City {

		//Coordinates
		public double xCoor;
		public double yCoor;

		public City(double x, double y) {
			xCoor = x;
			yCoor = y;
		}

		public double getXCoor() {
			return xCoor;
		}

		public double getYCoor() {
			return yCoor;
		}

		public void setXCoor(double x) {
			xCoor = x;
		}

		public void setYCoor(double y) {
			yCoor = y;
		}

	}
